function switchItUp(number) {
  const NUM_AS_STRING = [
    ["Zero"],
    ["One"],
    ["Two"],
    ["Three"],
    ["Four"],
    ["Five"],
    ["Six"],
    ["Seven"],
    ["Eight"],
    ["Nine"],
  ];

  return NUM_AS_STRING[number];
}
